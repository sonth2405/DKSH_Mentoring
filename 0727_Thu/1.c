// 관계 연산자

#include <stdio.h>

int main(void){

  int x = 3;
  int y = 5;

  printf(" x > y : %d\n", x > y);
  printf(" x >= y : %d\n", x >= y);

  printf(" x < y : %d\n", x < y);
  printf(" x <= y : %d\n", x <= y);

  printf(" x == y : %d\n", x == y);
  printf(" x != y : %d\n", x != y);


  return 0;
}