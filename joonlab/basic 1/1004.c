/*
정수 범위 조건에 따른 자료형 선택 -> long long 사용
*/

int main(void){
  long long int a, b, c, d, e, f, g, h, i, j;

  scanf("%lld %lld %lld %lld %lld %lld %lld %lld %lld %lld", &a, &b, &c, &d, &e, &f, &g, &h, &i, &j);

  printf("%lld %lld %lld %lld %lld %lld %lld %lld %lld %lld", a, b, c, d, e, f, g, h, i, j);
  
  return 0;
}